def main() -> int:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    en = TRANSLATIONS["en"]
    missing_keys: list[str] = []
    for lang, tbl in TRANSLATIONS.items():
        for k in KEYS:
            if k not in tbl:
                missing_keys.append(f"{lang}:{k}")
                tbl[k] = en.get(k, k)  # backfill from en
    if missing_keys:
        print(f"WARN: {len(missing_keys)} keys backfilled from en:")
        for m in missing_keys:
            print(f"  - {m}")
    for lang, tbl in TRANSLATIONS.items():
        path = OUT_DIR / f"{lang}.json"
        path.write_text(
            json.dumps(tbl, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )
        print(f"wrote {path.name} ({len(tbl)} keys)")
